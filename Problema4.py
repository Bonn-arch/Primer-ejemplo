#Operaciones con exponentes
print('*** Operaciones con exponentes ***')
puntuación = 2 ** 2      # la puntuación es 4
puntuación = 2 ** 3      # la puntuación ahora es 8
puntuación = 2 ** 4      # la puntuación ahora es 16
puntuación = 2 ** 5      # la puntuación ahora es 32

print(puntuación)        # Salida: 32
