#Calcular el imc de una persona
print('**** Cálculo del IMC ***')
# IMC = peso / altura^2
peso = 84  # en kilogramos
altura = 1.76  # en metros
imc = peso / (altura ** 2)  # IMC = peso / altura^2
print(imc)  # Salida: 27.073906485671232

