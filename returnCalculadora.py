def add(a, b):
    return a + b    
print(add(3, 5))  # Output: 8
def subtract(a, b):
    return a - b   
print(subtract(10, 4))  # Output: 6
def multiply(a, b):
    return a * b
print(multiply(2, 6))  # Output: 12
def divide(a, b):
    return a / b
print(divide(8, 2))  # Output: 4.0
def exp(a, b):
    return a ** b   
print(exp(3, 4))  # Output: 81